    private void cariButtonObatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariButtonObatActionPerformed
        modelSearchingObat.clear();
        HasilCariObat.setModel(modelSearchingObat);
        try{
            Connection getObat = DriverManager.getConnection("jdbc:mysql://localhost/cerdik","root","");
            Statement statement = getObat.createStatement();
            String queryGetObat = "SELECT NAMA FROM OBAT WHERE NAMA LIKE " + "'%" + cariObatSearchbar.getText().substring(0,3)+ "%'";
            ResultSet result = statement.executeQuery(queryGetObat);
            while(result.next()){
                modelSearchingObat.addElement(result.getString("NAMA"));
            }
            HasilCariObat.setModel(modelSearchingObat);
            getObat.close();
            statement.close();
        }catch(Exception e){
            System.out.println("Error! " + e);
        } 
    }//GEN-LAST:event_cariButtonObatActionPerformed

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        getAllObatID();
        if(modelListJadwal.size() > 0){
            try{
                //looping untuk semua obat di listjadwalobat
                for(int i = 0; i <= listIDObat.size(); i++ ){
                    Connection insertDataJadwal = DriverManager.getConnection("jdbc:mysql://localhost/cerdik","root","");
                    Statement statement = insertDataJadwal.createStatement();
                    String insertJadwalQuery = "INSERT INTO JADWAL VALUES(" + createID() + ", " +
                            Integer.valueOf(IDPasienTextField.getText()) + ", " +
                            jadwalPasien.saveIDNakes + ", " +
                            45152 + ", " +
                            listIDObat.get(i) + ", '" +
                            gejalaTextField2.getText() + "', '" +
                            inputDosisObat2.getText() + "', '" +
                            jadwalMulaiTextField2.getText() + "', '" +
                            jadwalSelesaiTextField2.getText() + "', 1, 0)";
                    statement.executeUpdate(insertJadwalQuery);
                    statement.executeUpdate(insertJadwalQuery);
                    insertDataJadwal.close();
                    statement.close();
                }  
            }catch(Exception e){
                System.out.println("Error! " + e);
            }
            JOptionPane.showMessageDialog(this,"Data telah terinput");
            jadwalPasien gantiFrame = new jadwalPasien();
            gantiFrame.getAllPasien();
            gantiFrame.setLocationRelativeTo(null);
            gantiFrame.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this,"Tidak ada obat yang terinput!");
        }
        
        
    }//GEN-LAST:event_submitButtonActionPerformed

