    public int createID(){
        int min = 99, max = 1000;
        return (min + (int)(Math.random() * ((max-min)+1)));
    }
    
    public void getAllObatID(){
        try{
            for(int i = 0; i < modelListJadwal.getSize(); i++){
               Connection getALLObat = DriverManager.getConnection("jdbc:mysql://localhost/cerdik","root","");
               Statement statement = getALLObat.createStatement();
               String queryGetObat = "SELECT IDOBAT FROM OBAT WHERE NAMA = '" + modelListJadwal.getElementAt(i).toString() + "'";
               ResultSet result = statement.executeQuery(queryGetObat); 
               result.first();
               listIDObat.add(result.getInt("IDOBAT"));
               getALLObat.close();
               statement.close();
            }
        }catch(Exception e){
            System.out.println("Error! " + e);
        }
    }
